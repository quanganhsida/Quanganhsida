    private void login(){
        String username = txtUserName.getText();
        String password = String.valueOf(txtPassword.getPassword());
        try {
            // Tạo kết nối
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/demo1", "root", "");
            Statement stm = con.createStatement();
            String sql = "select * from demo1 where User = '"+username+"' and Password ='"+password+"'";
            ResultSet rs = stm.executeQuery(sql);
                if(rs.next()){
                    dispose();
                    con.close();
                    GiaoDien GD = new GiaoDien();
                    GD.show();
                }else {
                    JOptionPane.showMessageDialog(rootPane, "Sai thông tin đăng nhập","Warning",JOptionPane.WARNING_MESSAGE);
                }
        } 
                catch(SQLException | ClassNotFoundException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Có lỗi xảy ra vui lòng kiểm tra lại","Warning",JOptionPane.ERROR_MESSAGE);
        }
    }
  
