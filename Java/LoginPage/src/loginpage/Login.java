    private void login(){
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        
        try{
            //get a connection
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/demo1", "root", "");
            //create a statement
            Statement stm = conn.createStatement();
            //execute sql query
            String sql = "select * from demo1 where User = '" + username + "' and Password = '" + password +"'";
            ResultSet rs = stm.executeQuery(sql);
            if(rs.next()){
                dispose();
                conn.close();
                homePage hpage = new homePage();
                hpage.show();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Wrong username or password!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(SQLException | ClassNotFoundException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Login error!", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }     
   
